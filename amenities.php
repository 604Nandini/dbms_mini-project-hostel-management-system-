</center><br><br>
<p class="paragraph">The Reverse Osmosis Plant installed in the Satyabama Campus, caters to the drinking water needs of all the Students, Teachers, supporting Staff and the Visitors. The raw water with an average Total Dissolved Solids [TDS] of 750-1000 ppm is treated to reduce the TDS content to less than 100 ppm the generally acceptable upper limit of the TDS
This is done in Three stages, namely: Pretreatment, passage of the water through 6 RO membrane filters held in parallel to each other{main process] and Post treatment. The maximum capacity of the plant is 1500 litres per hour [at the rate of 250 lit per membrane] The utilized capacity is 1000 lit/hr potable water. This water is Pure, Tasty and Hygienic rendering it highly suitable for human consumption.
The rejects water from the plant is used for gardening and washing purposes. This plant not only facilitates the supply of safe drinking water but also obviates the otherwise involved huge expenditure for procuring drinking water for the large number of people in the campus. Now the institution can be liberal in the provision of our own made RO water.
66 coolers are connected to this plant providing drinking water in every floor of class room blocks, administrative block, library and  laboratories Etc..
</p>	
