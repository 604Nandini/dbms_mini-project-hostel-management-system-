<h2>Mineral Water Facility</h2><br><center>
<img src="images/mineral-water.jpg" width="350px" height="200px" style="display: inline-block; padding-left: 30px;">
<img src="images/mineral-water2.jpg" width="350px" height="200px" style="display: inline-block; padding-left: 30px;">
